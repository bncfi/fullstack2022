const typeDefs = `
  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]
    allAuthors: [Author!]
  }

  type Author {
    name: String!
    born: Int
    bookCount: Int!
  }

  type Book {
    title: String!
    published: Int!
    author: Author!
    id: ID!
    genres: [String!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]
    ): Book
    editAuthor(name: String!, setBornTo: Int): Author
  }
`

const resolvers = {
  Mutation: {
    addBook: async (root, args) => {
      console.log('testi: ', Author.find({ name: args.author }))
      if (!Author.find({ name: args.author })) {
        const author = new Author({ name: args.author })
        author.save()
      }
      const author = Author.find({ name: args.author })
      console.log(author)
      const book = new Book({ ...args, author: author })
      try {
        book.save()
      } catch (error) {
        throw new GraphQLError('Saving book failed', {
          extensions: {
            code: 'BAD_USER_INPUT',
            invalidArgs: args.name,
            error,
          },
        })
      }
      return book
    },
    editAuthor: (root, args) => {
      authorIndex = authors.findIndex((author) => author.name === args.name)
      if (authors[authorIndex]) {
        authors[authorIndex].born = args.setBornTo
        return authors[authorIndex]
      }

      return null
    },
  },
  Query: {
    bookCount: async () => Book.collection.countDocuments(),
    authorCount: async () => Author.collection.countDocuments(),
    allBooks: async (root, args) => {
      if (Object.keys(args).length === 0) {
        return Book.find({})
      }
      if (args.author && args.genre) {
        return books
          .filter((book) => book.author === args.author)
          .filter((book) => book.genres.includes(args.genre.toLowerCase()))
      }
      if (args.author) {
        return Book.find({ author: args.author })
      }
      /*
      if (args.author) {
        return books.filter((book) => book.author === args.author)
      }*/
      if (args.genre) {
        return books.filter((book) =>
          book.genres.includes(args.genre.toLowerCase())
        )
      }
    },
    allAuthors: async () => {
      return Author.find({})
    },
  },

  Author: {
    bookCount: (root) => {
      return books.filter((book) => book.author === root.name).length
    },
  },
}

const server = new ApolloServer({
  typeDefs,
  resolvers,
})

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`)
})
